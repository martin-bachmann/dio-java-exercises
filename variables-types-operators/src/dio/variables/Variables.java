package dio.variables;

public class Variables {
	public static void main(String[] args) {
		int i;
		
		int nomeCorreto;
		
		final int NOME_CONSTANTE;
		
		i = 1;
		
		nomeCorreto = 2;
		
		NOME_CONSTANTE = 3;
		
		int outraVariavel = 4;
		
		final int OUTRA_CONSTANTE = 5;
		
		System.out.println(i);
		System.out.println(nomeCorreto);
		System.out.println(NOME_CONSTANTE);
		System.out.println(outraVariavel);
		System.out.println(OUTRA_CONSTANTE);
	}
}
