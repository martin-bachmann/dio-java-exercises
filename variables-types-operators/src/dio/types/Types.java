package dio.types;

public class Types {
	@SuppressWarnings("unused")
	public static void main(String[] args) {
		// Numeric types
		// byte: -128 to 127
		byte b1 = 15;
		byte b2 = 20;
		
		// short: -32.768 to 32767
		short s1 = -15785;
		short s2 = 324;
		
		// int: integer of 32 bits
		int i1 = 8515785;
		int i2 = -4242;
		
		// long: integer of 64 bits
		long l1 = 34134134;
		long l2 = -413412342;
		
		// float: reals of 32 bits
		float f1 = 3.1342f;
		float f2 = -424.44f;
		
		// double: reals of 64 bits
		double d1 = 342.13414d;
		double d2 = 42341.413444;
		
		// Character types
		// char
		char c1 = 'T';
		char c2 = '\u0057';
		
		// String
		String str1 = "texto";
		String str2 = "ad2134";
		
		// Logical types
		// boolean
		boolean bool1 = true;
		boolean bool2;
	}
}
